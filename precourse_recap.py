Simpsons = input("What town do The Simpsons live in?")

if Simpsons == "Springfield":
    print("WooHoo!")
elif Simpsons == "New York":
    print("New York is that way, man!")
elif Simpsons == "Shelbyville":
    print("Close, but no cigar")
else:
    print("DOH!")
